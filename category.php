		<h2><strong>Engineered</strong></h2>
		<p>Engineered wood floors are real wood floors that are manufactured using three to nine layers of different wood veneers. The sub layers can be of the same or different species. The top layer consists of high-quality wood. The grain of each layer runs in different directions, which makes the floor very stable. This means the wood will expand and contract less than solid wood flooring during fluctuations in humidity and temperature. Engineered floors are especially appropriate for Louisville’s varying temperatures and humidity.</p>
		<p>While this type of floor can be sanded and refinished, the process cannot be done as many times as it can with solid wood flooring. The number of times it can be sanded and refinishes depends on the thickness of the top layer. Engineered wood flooring is glued to the sub-floor or concrete and can be installed above, on, or below grade.</p>
